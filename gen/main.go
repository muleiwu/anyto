func needsTime(types []typeEntry) bool {
	for _, t := range types {
		if t.GoType == "time.Time" || t.GoType == "time.Duration" || t.GoType == "[]time.Duration" {
			return true
		}
	}
	return false
}

var funcMap = template.FuncMap{
	"needsTime": func() bool { return needsTime(types) },
}

const valueTmpl = `// Code generated by gen/main.go; DO NOT EDIT.

package anyto

import (
	{{- if needsTime }}
	"time"
	{{- end }}

	"github.com/spf13/cast"
)
{{range .}}
// {{.MethodName}} converts the value to {{.GoType}}, returning the zero value on error.
func (a *anyValue) {{.MethodName}}() {{.GoType}} {
	v, _ := {{.CastFuncE}}(a.v)
	return v
}
{{end}}`

const valueETmpl = `// Code generated by gen/main.go; DO NOT EDIT.

package anyto

import (
	{{- if needsTime }}
	"time"
	{{- end }}

	"github.com/spf13/cast"
)
{{range .}}
// {{.MethodName}} converts the value to {{.GoType}}, returning an error on failure.
func (a *anyValueE) {{.MethodName}}() ({{.GoType}}, error) {
	return {{.CastFuncE}}(a.v)
}
{{end}}`

const pointerTmpl = `// Code generated by gen/main.go; DO NOT EDIT.

package anyto

import (
	{{- if needsTime }}
	"time"
	{{- end }}

	"github.com/spf13/cast"
)
{{range .}}
// {{.MethodName}} converts the value to *{{.GoType}}, returning nil on error.
func (a *anyPointer) {{.MethodName}}() *{{.GoType}} {
	v, err := {{.CastFuncE}}(a.v)
	if err != nil {
		return nil
	}
	return &v
}
{{end}}`

const pointerETmpl = `// Code generated by gen/main.go; DO NOT EDIT.

package anyto

import (
	{{- if needsTime }}
	"time"
	{{- end }}

	"github.com/spf13/cast"
)
{{range .}}
// {{.MethodName}} converts the value to *{{.GoType}}, returning (nil, error) on failure.
func (a *anyPointerE) {{.MethodName}}() (*{{.GoType}}, error) {
	v, err := {{.CastFuncE}}(a.v)
	if err != nil {
		return nil, err
	}
	return &v, nil
}
{{end}}`

var templates = map[string]string{
	"any_value_gen.go":     valueTmpl,
	"any_value_e_gen.go":   valueETmpl,
	"any_pointer_gen.go":   pointerTmpl,
	"any_pointer_e_gen.go": pointerETmpl,
}

func main() {
	for filename, tmplStr := range templates {
		t := template.Must(template.New(filename).Funcs(funcMap).Parse(tmplStr))

		var buf bytes.Buffer
		if err := t.Execute(&buf, types); err != nil {
			panic(err)
		}

		src, err := format.Source(buf.Bytes())
		if err != nil {
			panic(err)
		}

		if err := os.WriteFile(filename, src, 0644); err != nil {
			panic(err)
		}
	}
}
